package ar.com.osde.integracionsaludsoft.dto;

/**
 * The Class ServicioFilter.
 */
public class ServicioFilter {

	/** The nombre. */
	private String nombre;

	/** The codigo. */
	private String codigo;

	/**
	 * Instantiates a new servicio filter.
	 */
	public ServicioFilter() {
		super();
	}

	/**
	 * Gets the nombre.
	 *
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * Sets the nombre.
	 *
	 * @param nombre
	 *            the new nombre
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * Gets the codigo.
	 *
	 * @return the codigo
	 */
	public String getCodigo() {
		return codigo;
	}

	/**
	 * Sets the codigo.
	 *
	 * @param codigo
	 *            the new codigo
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

}
